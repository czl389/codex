# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, x):
#         self.val = x
#         self.next = None

class Solution:
    def detectCycle(self, head: Optional[ListNode]) -> Optional[ListNode]:
        fast = slow = head
        while fast and slow:
            slow = slow.next
            if fast.next is None:
                return None
            fast = fast.next.next
            if fast is None:
                return None
            if fast == slow:
                break
        slow2 = head
        while slow and slow2:
            if slow == slow2:
                return slow2
            slow = slow.next
            slow2 = slow2.next
        return None
        
